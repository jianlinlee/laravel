<?php
/**
 * 公用规则校验，用于存储通用的校验方法
 * 例：手机号格式校验、身份证号校验等
 */

namespace App\Common\Rules;

class FormatCheck
{
    /**
     * 手机号校验
     * 移动号码段:139、138、137、136、135、134、150、151、152、157、158、159、182、183、187、188、147
     * 联通号码段:130、131、132、136、185、186、145
     * 电信号码段:133、153、180、189
     * @param $phone
     */
    public static function isCellPhone($phone = '') {
        return preg_match("/^((13[0-9])|(14[5|7])|(15([0-3]|[5-9]))|(18[0,5-9]))\d{8}$/", $phone);
    }
}